string = input("Enter a sentance: ")
#upper case print like: RITIK
print(string.upper())
print()
#lower case print: ritik
print(string.lower())
print()
#upper to lower case like: My Name Is Ritik.
print(string.title())
print()
#split
print(string.split())
print()
#startwith
print(string.startswith('a'))
print()
#sprit
print(string.strip)
print()
#find
print(string.find('a'))
print()
#count
print(string.count('a'))
print()
import math

num = 25
decimal = 7.8

print("Ceil:", math.ceil(decimal)) #Decimal number ko upar wali nearest integer me convert karta hai.
print("Floor:", math.floor(decimal)) #Decimal number ko neeche wali nearest integer me convert karta hai.
print("Square Root:", math.sqrt(num)) #Square root nikalta hai.
print("Value of Pi:", math.pi) # π (Pi) ki value deta hai.

# Any two additional functions
print("Power (2^5):", math.pow(2, 5))  #Power nikalta hai.

print("Factorial of 7:", math.factorial(7)) # factorial nikalta hai. 

